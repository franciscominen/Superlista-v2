import type { ReactElement } from 'react'
import Layout from '@/components/layout'
import type { NextPageWithLayout } from '@/pages/_app'
import Head from 'next/head'
import { useList } from '~/hooks'
import ProductListCard from '~/components/productListCard'
import { IProduct } from '~/resources/types'

const MyList: NextPageWithLayout = () => {

    const list = useList()

    return (
        <>
            <Head>
                <title>Superlista.ar | Productos</title>
                <meta name="description" content="Generated by create next app" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            {
                !list.length ?
                    <p>Tu Lista esta vacia.</p> :
                    list.map((product: IProduct) => {
                        return (
                            <ProductListCard
                                id={product.id}
                                name={product.name}
                                img={product.img}
                                note={product.note}
                                category={product.category}
                                key={product.name}
                                product={product}
                            />
                        )
                    })
            }
        </>
    )
}

MyList.getLayout = function getLayout(page: ReactElement) {
    return (
        <Layout>
            {page}
        </Layout>
    )
}

export default MyList
